package unit1;

public class Unit1Task0 {
    public static void main(String[] args) {

        int i=0;
        while(i<11){
            System.out.print(i+" ");
            i++;
        }

        System.out.println();

        for(int k=0;k<11;k++){
            System.out.print(k+" ");
        }

        System.out.println();

        int l=0;
        do {
            System.out.print(l+" ");
            l++;
        }
        while (l<11);
    }
}
